import UIKit

extension UIColor {
    static let customBlack: UIColor = .init(named: "CustomBlack") ?? .black
    static let customGray: UIColor = .init(named: "CustomGray") ?? .gray
    static let customLightGray: UIColor = .init(named: "CustomLightGray") ?? .lightGray
}
